class Prog02 {
  public static void main(String[] args) {
    // declaracao de variaveis
    int v1 = 10, v2 = 3;
    String v3 = "100";

    // exibindo valores
    System.out.println("Valor de v1: " + v1);
    System.out.println("Valor de v2: " + v2);

    // realizando algumas operacoes
    System.out.println("\nResultado adicao: " + (v1 + v2));
    System.out.println("Resultado multiplicacao: " + (v1 * v2));
    System.out.println("Resultado resto div: " + (v1 % v2));
    System.out.println("Resultado adicao 2: " + (v1 + v2 + v3));
    System.out.println("\nFim do programa.");
    
 }
}
